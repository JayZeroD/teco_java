package basic.ch01;

public class HelloWorld {

	// <- 주석, 한 줄 주석(컴파일러가 무시하는 영역)
	public static void main(String[] args) {

		// 화면에(콘솔 창에) 출력하라는 명령어
		System.out.println("Hello World");

		System.out.println();// 출력후 다음 줄로 넘어감
		// 숫자 0부터 9까지 화면에 하나식 출력 하시오
		System.out.println(0);
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		System.out.println(5);
		System.out.println(6);
		System.out.println(7);
		System.out.println(8);
		System.out.println(9);

		// 우리 일상에서는 10진수라는 개념을 많이 사용하고 있습니다
		// 10진수란 뭘까?
		// 0 ~ 9 까지의 숫자를 사용하여 수를 표한하는 기법
		// 0 ~ 9 로 단위내 숫자를 표현하고 범위를 넘어가면 단위를
		// 바꾸어 표현하는 개념.
		
		
		
		
		// 그럼 2진수란
		// 0과 1로만 이루어진 숫자 체계
		// 2진수로 2를 표현한다면
		// 1 - > 2  단위 1을 올림 --> 10 (10진수의 2)
		// 2진수로 3을 표현하려면
		// 1 -> 10 -> 11  (10진수의 숫자 3)
		
		System.out.println("코드를 수정했어");
		
	}// end of main

}// end of class
